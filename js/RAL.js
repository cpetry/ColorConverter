    findBestHexValue(dict, hex, hexString, best){
        var rgbIdeal = hex.hexToRgb(hexString);
        for (var k in dict) {
            var hexValue = dict[k];
            if (best.key != null)
            {
                var rgb = hex.hexToRgb(hexValue);
                var currentDistance = this.getDistance(rgbIdeal, rgb)
            }
            else{
                best.key = hexValue;
                continue;
            }

            if (currentDistance < best.value){
                best.key = hexValue;
                best.value = currentDistance;
                //console.log("Best " + best.key + ":" + best.value);
            }
        }
    }

    getRALString(hexString){
        hexString = hexString.replaceAll(' ', '').trim().toUpperCase();

        for (var c in classicDict) {
            if (classicDict[c] == hexString)
                return c;
        }

        for (var d in designDict) {
            if (designDict[d] == hexString)
                return d;
        }

        console.log("Could not find RAL code for '" + hexString + "'");
    }

    setHexString(hexString){
        var ralString = this.getRALString(hexString);
        if (ralString === undefined)
            return

        ralString = ralString.replace(/RAL(\d{4})$/, "RAL $1")
        ralString = ralString.replace(/RAL(\d{3})(\d{2})(\d{2})$/, "RAL $1 $2 $3")

        var ralInput = this.getRALElement();
        ralInput.value = ralString;
    }

    setHexStringLinear(hexString){
        var ralString = this.getRALString(hexString);
        if (ralString === undefined)
            return

        ralString = ralString.replace(/RAL(\d{4})$/, "RAL $1")
        ralString = ralString.replace(/RAL(\d{3})(\d{2})(\d{2})$/, "RAL $1 $2 $3")

        var ralInput = this.getRALElementLinear();
        ralInput.value = ralString;
    }

    getDistance(rgb, otherRgb){
        return Math.abs(rgb.r - otherRgb.r) + Math.abs(rgb.g - otherRgb.g) + Math.abs(rgb.b - otherRgb.b)
    }
